public class type_casting {
    public static void main(String args[])
    {
        byte a=5;
        int b=(int)a;
        float c=10.5f;
        double d=(double)c;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
    
}
